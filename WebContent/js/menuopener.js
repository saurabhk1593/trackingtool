// Functions to Expand and close the submenus

function main1_opener()
{
if(document.getElementById('main1_sub').style.display =='block')
{
document.getElementById('main1_sub').style.display = 'none';
}
else
{
document.getElementById('main1_sub').style.display ='block';
}
}

